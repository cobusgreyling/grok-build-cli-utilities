@dataclass
class Hook:
    event: str
    path: Path
    scope: str = "user"
    entries: int = 0
    commands: list[str] = field(default_factory=list)
    description: str = ""


def parse_hooks(hooks_file: Path, scope: str = "user") -> list[Hook]:
    """Parse a hooks.json file. Returns list of Hook per event."""
    if not hooks_file.exists():
        return []
    text = safe_read_text(hooks_file)
    if not text:
        return []
    try:
        data = json.loads(text)
    except (json.JSONDecodeError, TypeError, UnicodeDecodeError):
        return []

    desc = data.get("description", "") if isinstance(data, dict) else ""
    hooks_section = data.get("hooks", {}) if isinstance(data, dict) else {}
    results: list[Hook] = []
    if isinstance(hooks_section, dict):
        for event, entries in hooks_section.items():
            cmds: list[str] = []
            count = 0
            if isinstance(entries, list):
                for e in entries:
                    if isinstance(e, dict):
                        for h in e.get("hooks", []):
                            if isinstance(h, dict) and h.get("type") == "command":
                                c = h.get("command", "")
                                if c:
                                    cmds.append(str(c)[:120])
                            count += 1
            results.append(
                Hook(
                    event=str(event),
                    path=hooks_file,
                    scope=scope,
                    entries=count or len(cmds),
                    commands=cmds[:5],
                    description=str(desc)[:120],
                )
            )
    return results


def iter_hooks(grok_home: Path) -> list[Hook]:
    """Collect all hooks from known hook dirs and also from inside discovered plugins."""
    from .common import get_hooks_dirs

    out: list[Hook] = []
    for scope, hdir in get_hooks_dirs(grok_home):
        if not hdir or not hdir.exists():
            continue
        for jf in sorted(hdir.glob("*.json")):
            out.extend(parse_hooks(jf, scope=scope))
        for jf in hdir.rglob("hooks.json"):
            out.extend(parse_hooks(jf, scope=scope))

    # Also pull from plugins that ship hooks
    try:
        for pl in iter_plugins(grok_home):
            ph = pl.path / "hooks" / "hooks.json"
            if ph.exists():
                for hk in parse_hooks(ph, scope=f"plugin:{pl.name}"):
                    out.append(hk)
    except Exception:
        pass
    return out


