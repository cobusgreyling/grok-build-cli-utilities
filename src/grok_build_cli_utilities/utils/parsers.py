# Simple project rules / AGENTS discovery (Tier 2/3 stub)
# -----------------------------
def iter_project_rules() -> list[Path]:
    """Find AGENTS.md or .grok/rules in cwd/repo (very lightweight)."""
    out: list[Path] = []
    cwd = Path.cwd()
    for cand in [cwd / "AGENTS.md", cwd / ".grok" / "rules.md", cwd / "rules.md"]:
        if cand.exists():
            out.append(cand)
    repo = find_repo_root()
    if repo:
        for cand in [repo / "AGENTS.md", repo / ".grok" / "rules.md"]:
            if cand.exists() and cand not in out:
                out.append(cand)
    return out
