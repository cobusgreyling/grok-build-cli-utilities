@dataclass
class SessionSignals:
    session_id: str
    turn_count: int = 0
    tool_call_count: int = 0
    error_count: int = 0
    compaction_count: int = 0
    context_tokens_used: int = 0
    context_window_tokens: int = 0
    tools_used: list[str] = field(default_factory=list)
    models_used: list[str] = field(default_factory=list)
    session_duration_s: int = 0
    agent_files_touched: int = 0
    raw: dict[str, Any] = field(default_factory=dict)


def load_session_signals(session_path: Path) -> Optional[SessionSignals]:
    sigf = session_path / "signals.json"
    if not sigf.exists():
        return None
    text = safe_read_text(sigf)
    if not text:
        return None
    try:
        data = json.loads(text)
        return SessionSignals(
            session_id=session_path.name,
            turn_count=data.get("turnCount", 0),
            tool_call_count=data.get("toolCallCount", 0),
            error_count=data.get("errorCount", 0),
            compaction_count=data.get("compactionCount", 0),
            context_tokens_used=data.get("contextTokensUsed", 0),
            context_window_tokens=data.get("contextWindowTokens", 0),
            tools_used=data.get("toolsUsed", []) or [],
            models_used=data.get("modelsUsed", []) or [],
            session_duration_s=data.get("sessionDurationSeconds", 0),
            agent_files_touched=data.get("agentFilesTouched", 0),
            raw=data,
        )
    except (json.JSONDecodeError, TypeError, KeyError, ValueError):
        return None


@dataclass
class RewindPoint:
    prompt_index: int
    created_at: Optional[str] = None
    num_file_snapshots: int = 0


def load_rewind_points(session_path: Path, limit: int = 20) -> list[RewindPoint]:
    rpf = session_path / "rewind_points.jsonl"
    if not rpf.exists():
        return []
    out: list[RewindPoint] = []
    text = safe_read_text(rpf)
    if not text:
        return out
    for i, line in enumerate(text.splitlines()):
        if i >= limit:
            break
        try:
            d = json.loads(line)
            out.append(
                RewindPoint(
                    prompt_index=d.get("prompt_index", i),
                    created_at=d.get("created_at"),
                    num_file_snapshots=len(d.get("file_snapshots", {})),
                )
            )
        except (json.JSONDecodeError, TypeError, KeyError, ValueError):
            continue
    return out


