# Logs (simple)
# -----------------------------
def tail_logs(grok_home: Path, lines: int = 50, level: Optional[str] = None) -> list[dict]:
    from .common import get_logs_path

    lp = get_logs_path(grok_home)
    if not lp.exists():
        return []
    out: list[dict] = []
    text = safe_read_text(lp)
    if not text:
        return out
    all_lines = text.splitlines()[-lines * 2 :]
    for raw in all_lines[-lines:]:
        try:
            d = json.loads(raw)
            if level and d.get("lvl", "").lower() != level.lower():
                continue
            out.append(d)
        except (json.JSONDecodeError, TypeError, KeyError, AttributeError):
            continue
    return out


