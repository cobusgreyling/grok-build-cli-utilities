@dataclass
class Plugin:
    name: str
    path: Path
    scope: str = "user"
    version: str = ""
    description: str = ""
    author: str = ""
    has_skills: bool = False
    has_agents: bool = False
    has_hooks: bool = False
    has_mcp: bool = False
    components: list[str] = field(default_factory=list)

    @property
    def is_valid(self) -> bool:
        return bool(self.name)


def _looks_like_plugin(p: Path) -> bool:
    if not p.exists():
        return False
    if p.is_dir():
        markers = [
            "skills",
            "agents",
            "hooks",
            ".mcp.json",
            ".lsp.json",
            "plugin.json",
            "manifest.json",
        ]
        return any((p / m).exists() for m in markers)
    return False


def parse_plugin(plugin_dir: Path, scope: str = "user") -> Optional[Plugin]:
    """Parse a plugin directory or manifest. Best effort, very tolerant."""
    if not _looks_like_plugin(plugin_dir):
        return None
    name = plugin_dir.name
    version = ""
    desc = ""
    author = ""
    manifest = plugin_dir / "plugin.json"
    if not manifest.exists():
        manifest = plugin_dir / ".claude-plugin" / "plugin.json"
    if manifest.exists():
        text = safe_read_text(manifest)
        data = safe_json_load(text, default={})
        name = data.get("name", name)
        version = data.get("version", "")
        desc = data.get("description", "")
        a = data.get("author", {})
        if isinstance(a, dict):
            author = a.get("name", "")
        elif isinstance(a, str):
            author = a

    has_skills = (plugin_dir / "skills").exists() or bool(list((plugin_dir).rglob("SKILL.md")))
    has_agents = (plugin_dir / "agents").exists()
    has_hooks = (plugin_dir / "hooks" / "hooks.json").exists() or (plugin_dir / "hooks").exists()
    has_mcp = (plugin_dir / ".mcp.json").exists()

    comps: list[str] = []
    if has_skills:
        comps.append("skills")
    if has_agents:
        comps.append("agents")
    if has_hooks:
        comps.append("hooks")
    if has_mcp:
        comps.append("mcp")

    return Plugin(
        name=str(name).strip(),
        path=plugin_dir,
        scope=scope,
        version=version,
        description=desc[:200],
        author=author,
        has_skills=has_skills,
        has_agents=has_agents,
        has_hooks=has_hooks,
        has_mcp=has_mcp,
        components=comps,
    )


def iter_plugins(grok_home: Path, include_marketplace: bool = False) -> list[Plugin]:
    """Discover plugins from standard locations. Dedup by name (higher scope wins)."""
    from .common import get_plugin_dirs
    import os as _os  # local alias to avoid top level if not wanted

    seen: set[str] = set()
    results: list[Plugin] = []
    for scope, pdir in get_plugin_dirs(grok_home):
        if not pdir or not pdir.exists():
            continue
        for child in sorted(pdir.iterdir()):
            if child.name.startswith("."):
                continue
            pl = parse_plugin(child, scope=scope)
            if pl and pl.name and pl.name.lower() not in seen:
                seen.add(pl.name.lower())
                results.append(pl)

    if include_marketplace:
        try:
            import os as _os

            cache = grok_home / "marketplace-cache"
            if cache.exists():
                for root, dirs, _ in _os.walk(cache):
                    for d in dirs:
                        cand = Path(root) / d
                        if _looks_like_plugin(cand) or (cand / ".claude-plugin").exists():
                            pl = parse_plugin(cand, scope="marketplace")
                            if pl and pl.name.lower() not in seen:
                                seen.add(pl.name.lower())
                                results.append(pl)
        except (OSError, PermissionError, RuntimeError):
            pass
    return results


def validate_plugin(pl: Plugin) -> list[str]:
    errs: list[str] = []
    if not pl.name:
        errs.append("missing name")
    if not (pl.has_skills or pl.has_agents or pl.has_hooks or pl.has_mcp):
        errs.append("no recognizable components (skills/agents/hooks/mcp)")
    return errs


